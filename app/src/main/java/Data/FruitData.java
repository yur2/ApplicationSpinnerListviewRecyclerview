public class FruitData {
    String FruitName;
    String FruitDesc;
    int FruitImg;
    int DetailImg;

    public int getDetailImg() {
        return DetailImg;
    }

    public void setDetailImg(int detailImg) {
        DetailImg = detailImg;
    }

    public String getFruitName() {
        return FruitName;
    }

    public void setFruitName(String fruitName) {
        FruitName = fruitName;
    }

    public String getFruitDesc() {
        return FruitDesc;
    }

    public void setFruitDesc(String fruitDesc) {
        FruitDesc = fruitDesc;
    }

    public int getFruitImg() {
        return FruitImg;
    }

    public void setFruitImg(int fruitImg) {
        FruitImg = fruitImg;
    }

}
